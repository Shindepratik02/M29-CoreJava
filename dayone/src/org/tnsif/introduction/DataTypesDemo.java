package org.tnsif.introduction;

public class DataTypesDemo {

	public static void main(String[] args) {
		int num1=5/2;
		float num2=102/45;
		double num3=16d/7d; //d will give precision upto 7 values//
		float num4=10f/3f; //f will give precision upto 16 values
		System.out.println("value1 is "+num1);
		System.out.println("value2 is "+num2);
		System.out.println("value3 is "+num3);
		System.out.println("value4 is "+num4);
		
	}

}
